import axios from 'axios';


export const baseUrl = process.env.NODE_ENV === 'development'?'http://localhost:8084':'http://localhost:8084';


const service = axios.create({
    baseURL: baseUrl,
    timeout: 5000
});

service.interceptors.request.use(
    config => {
        return config;
    },
    error => {
        console.log(error);
        return Promise.reject();
    }
);

service.interceptors.response.use(
    response => {
        if (response.status === 200) {
            return response.data;
        } else {
            Promise.reject();
        }
    },
    error => {
        console.log(error);
        return Promise.reject();
    }
);

export default service;